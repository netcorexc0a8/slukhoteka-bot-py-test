    "Поток: клиент → его активный абонемент (или выдать новый) → дата → время.\n"
    "После одиночной записи бот предложит «🔁 Создать серию» — это "
    "создаст по 1 занятию в неделю на все оставшиеся занятия абонемента.\n\n"

