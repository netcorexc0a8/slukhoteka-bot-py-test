    "<b>Условные обозначения</b>\n"
    "🟢 — активный абонемент    ✅ — завершённый\n"
    "⏰ — просроченный    ⛔ — отменённый\n"
    "👤 — индивидуальное занятие    👥 — групповое\n"
    "[3/8] — порядковый номер занятия из общего количества в абонементе\n\n"

