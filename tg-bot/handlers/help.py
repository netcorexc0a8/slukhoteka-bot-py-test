    "<b>👥 Группа</b> — это объединение клиентов для совместных групповых "
    "занятий («Алгоритмика», «Чтение»). У группы есть постоянный состав, "
    "а ведущие назначаются на каждое конкретное занятие.\n\n"

    "<b>📅 Запись</b> — это занятие в расписании на конкретное время. "
    "Может быть индивидуальной (1 клиент) или групповой (часть группы).\n\n"

    "<b>━━━━━━━━━━━━━━━━━━━━━━</b>\n\n"

    "<b>📅 Расписание — основной раздел.</b>\n"
    "Все действия с занятиями, абонементами и группами здесь.\n\n"

